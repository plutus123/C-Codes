#include <stdio.h>
int main(){
    int x;
    int y;
    int z;
    int k;
    printf("Enter the value of x\n");
    scanf("%d",&x);
    printf("The value of 3*x is %d\n",3*x);
    printf("Enter the value of y\n");
    scanf("%d",&y);
    printf("The value of 3*x/y is %d\n",3*x/y);
    printf("Enter the value of z\n");
    scanf("%d",&z);
    printf("The value of 3*x/y-z is %d\n",3*x/y-z);
    printf("Enter the value of k\n");
    scanf("%d",&k);
    printf("The value of 3*x/y-z+k is %d\n",3*x/y-z+k);
    return 0;
}